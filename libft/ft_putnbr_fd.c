#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	buffer[11];
	int		i;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n = -n;
		write(fd, "-", 1);
	}
	i = 0;
	while (n > 9)
	{
		buffer[i++] = (n % 10) + '0';
		n /= 10;
	}
	buffer[i] = n + '0';
	while (i >= 0)
		write(fd, &buffer[i--], 1);
}
