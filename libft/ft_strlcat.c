#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t nb)
{
	size_t	dst_len;
	size_t	src_len;
	size_t	i;

	src_len = ft_strlen(src);
	i = 0;
	if (!dest && nb == 0)
		return (0);
	dst_len = ft_strlen(dest);
	if (nb <= dst_len)
		return (src_len + nb);
	while (i < src_len && dst_len + i + 1 < nb)
	{
		dest[dst_len + i] = src[i];
		i++;
	}
	dest[dst_len + i] = '\0';
	return (dst_len + src_len);
}
